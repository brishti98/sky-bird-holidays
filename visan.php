		<b>VISA PROCEDURE</b><br><br>
		<b>Tourist Visa:</b>
		<br><br>
		Visa can be obtained on arrival at the Tribhuvan International Airport, Kathmandu, at border entry points in Kakadvitta, Birgunj, Bhairahawa, Nepalgunj, Gaddachowki on Nepal-India border and Kodari on Nepal-China border. Visa can also be obtained at the nearest Nepal Embassy or Diplomatic Mission. Visa can also be obtained (renewal purposes) at Department of Immigration, Kalikasthan, and Kathmandu. A valid passport and one passport size photo with a light background is required. Immigration Department has not specified the size of the passport-size photo.
		<br><br>
		Visa can be obtained only through payment of cash in US Dollar. Credit card, Indian currency and Nepali currency are not accepted as payment of visa fee.
		<ol type="a">
			<li><b>Tourist Visa</b><br>Visa Facility Duration Fee<br>
				<ol type="1">
					<li>Multiple entry 15 days US$ 25 or equivalent convertible currency</li>
					<li>Multiple entry 30 days US$ 40 or equivalent convertible currency</li>
					<li>Multiple entry 90 days US$ 100 or equivalent convertible currency</li>
				</ol>
			</li><br>
			<li><b>Gratis (Free) Visa</b><br>
			For first visit in one visa year (January to December), gratis visa for 30 days is available only for nationals of South   Asian countries like Bangladesh, Bhutan, Maldives, Pakistan and Sri Lanka. However, visa can be extended from the Immigration Department on payment of visa fee as specified above.<br>
			Indian nationals do not require visa to enter into Nepal.</li><br>
			<li><b>For Indian National</b> <br>
				Indian nationals do not require visa to enter Nepal.<br><br>
				As per the Nepalese Immigration, Indian Nationals Travelling to Nepal must posses any one of the following documents.<br>
				<ol>
					<li>Passport</li>
					<li>Driving License with photo</li>
					<li>Photo Identity card issued by a Government Agency</li>
					<li>Ration Card with Photo</li>
					<li>Election Commission Card with Photo</li>
					<li>Identity Card issued by Embassy of India in Kathmandu</li>
					<li>Identity Card with Photo issued by Sub- Divisional Magistrate or any other officials above his rank. Also, please check with your nearest travel agents for documents required by the Indian Immigration for Indians traveling to Nepal.</li>
				</ol></li><br>
			<li><b>Other Information</b><br>
				Nationals from Nigeria, Ghana, Zimbabwe, Swaziland, Cameroon, Somalia, Liberia, Ethiopia, Iraq, Palestine and Afghanistan will need to obtain visa from Nepal Embassies or Diplomatic Missions in their respective countries, as they do not get visa on arrival at the immigration entry points of Nepal.</li><br>
			<li><b>Visa Extension</b><br>
				Tourists can stay for a maximum of 150 days in a visa year (Jan 1 to Dec 31).</li>
