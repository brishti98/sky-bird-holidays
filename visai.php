		<p id="info3">
		<b>STEP 3: PROCESS YOUR INDIAN VISA APPLICATION ONLINE</b><br>
		The Government of India only does E-Visa applications through this website: www.indianvisaonline.gov.in Take note that facilitation by any intermediary, agents, etc. is NOT required to process this kind of online Indian Visa. After all, it's easy and it's best that you do it yourself.<br>
		<br>
		In order to do the application, just follow the steps below:
		<br>
		<ol type="1">
			<li>Visit the E-Visa website at indianvisaonline.gov.in.</li>
			<li>Click the orange button that says "e-Visa Application". This will open up a short form which you will need to fill out. The rest of the fields are self-explanatory; what's helpful to note is that:
			<p id="info3a">
				- You must click the box beside "e-Tourist Visa" for the next prompt, select Recreation/Sightseeing (or select the other options if it's more applicable to your situation).
			</p>
			<li>Once done, check the box that says "I have read the instructions" and then click the orange button that says Continue.</li>
			<li>The next page will show you a longer form which asks for Applicant Details, Passport Details, Contact Details, Family Details, Visa Details, Purpose Details, Occupation Details, References, etc.  Some tips to remember while filling this out:
				<p id="info3a">
					-Under References, for the section where it asks for a reference in India, if you're not visiting a family or friend, you can enter here the details of the hotel that you'll be staying with. If you're staying at multiple hotels, you can mention the first one you're staying on.<br>
					-There are 2 pages for this section, you can accomplish it all and click Save and Continue or if you're missing some info, you can Save and Temporarily Exit
					. Remember that once you click Save and Continue, you can NO longer change any of the information, so make sure you've got everything right.
				</p>
			</li>
			<li>Once you're finished with the long form, the next pages will ask you to upload your photo AND your passport's bio page.</li>
			<li>Please take note of the specifications and file size limitations (as mentioned in Step #2; rest assured, the page will remind you of such).</li>
			<li>At this point, the website will give you the auto-generated Application ID # unique to you. Take note of this. (Rest assured, the details will be sent later on in an email, but it's good practice still to write this number down or take a screenshot of the page).</li>
		</ol>
		<p id="info3a">
		The next step now will be to make the e-Visa fee payment<br>
		<br>
		</p>
