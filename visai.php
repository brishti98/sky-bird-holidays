		<b>STEP 4: DO THE VISA FEE PAYMENT</b><br>
		On the payment portal click on the bullet that says Yes, click on Pay Now,and select payment gateway.<br>
		<br>
		HOW MUCH IS THE FEE?<br>
		It depends on the reciprocal relationship of your country and India. For a detailed fee chart, you can check here. Otherwise, the fee scheme is in 4 levels.
		<br>
		<ol>
			<li>FREE: Argentina, Cook Islands, Fiji, Jamaica, Kiribati, Marshall Islands, Mauritius, Micronesia, Nauru, Niue Island, Palau, Papua New Guinea, Samoa, Seychelles, Solomon Islands, South Africa, Tonga, Tuvalu, Uruguay and Vanuatu</li>
			<li>$75: USA, UK, Russia, Ukraine and Mozambique</li>
			<li>$50: Citizens of the majority of countries including Australia, Belgium, Brazil, Canada, China, Hong Kong, Finland, France, Germany, Hungary, Indonesia, Ireland, Israel, Jordan, Kenya, Malaysia, Mexico, Myanmar, the Netherlands, New Zealand, the Philippines, Poland, Portugal, Spain, Sweden, Taiwan, Thailand, Vietnam, etc.</li>
			<li>$25: Citizens of Japan, Singapore and Sri Lanka</li>
		</ol>
		</p>
		<p id="info4a">
		ANY ADDITIONAL CHARGES?<br>
		On top of the visa fee, you will need to also pay for the bank charge which is 2.5% of the value.<br>
		<br>
		IMPORTANT: Payment of this fee is NOT a guarantee that your visa will be approved; it is also non-refundable. In case of any double/triple payments, it will be refunded after due reconciliation within seven (7) days. Either way, please be careful while doing the payment process because if the number of unsuccessful attempts is more than 3, you would have to start afresh and redo the whole application form again with a new application ID #.<br>
		</p>
		<br>
		<br>
		<p id="info5">
		<b>STEP 5: WAIT FOR THE RESULTS OF YOUR E-VISA APPLICATION</b><br>
		After you complete the whole online application, you'll just have to wait for an email update from them that confirms that your E-Visa is Granted. If it is, they will proceed to send you your Electronic Travel Authorization or ETA.<br>
		<br>
		Normally, visa processing takes 72 hours.<br>
		<br>
		You need to print this ETA page out and bring it with you when you travel to India because it will serve as your papers once you're at the immigration counter in the airport.<br>
		<br>
		NOTE: To be entirely sure, please confirm that your ETA status is shown as Granted on this website before you fly. If there are any problems, you can contact their support centre at indiatvoa@gov.in.<br>
