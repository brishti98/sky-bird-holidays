<p>Trekking in India gives you innumerable opportunities to stay close to nature, and explore the charm of the geographical diversities of the country. Among the regions in India containing the most varied topography are the mighty Himalayan mountains. You may start hiking on the vast Northern Plains below the Shivalik foothills, and travel all the way to the highest points of the ranges. Trails through the foothills are generally easy, and not much elevation gain will be experienced by you.</p>
